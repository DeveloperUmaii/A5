// BG & TEXT Colour Change 
function removebgClour (idd){
    const seat = document.getElementById(idd)
    seat.classList.remove('bg-[#F7F8F8]');
}
function setbgcolor (idds){
    const seat = document.getElementById(idds)
    seat.classList.add('greenbg');
    console.log('zeh')   
}
function removeTxtClour (idd){
    const seat = document.getElementById(idd)
    seat.classList.remove('Text-Ash');
}
function setTxtColor (idds){
    const seat = document.getElementById(idds)
    seat.classList.add('text-white');
    console.log('White')   
}
